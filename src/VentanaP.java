private void buscarEnTabla(String valorBuscado) {
    for (int i = 0; i < jTable1.getRowCount(); i++) {
        for (int j = 0; j < jTable1.getColumnCount(); j++) {
            // Verifica si el valor en la celda es igual al valor buscado
            if (jTable1.getValueAt(i, j) != null && jTable1.getValueAt(i, j).toString().equalsIgnoreCase(valorBuscado)) {
                // Selecciona la fila donde se encuentra el valor
                jTable1.setRowSelectionInterval(i, i);
                jTable1.scrollRectToVisible(jTable1.getCellRect(i, 0, true));
                JOptionPane.showMessageDialog(this, "Dato encontrado", "Búsqueda", JOptionPane.INFORMATION_MESSAGE);
                return; // Detenemos la búsqueda después de encontrar la primera coincidencia
            }
        }
    }
    JOptionPane.showMessageDialog(this, "Dato no encontrado", "Búsqueda", JOptionPane.INFORMATION_MESSAGE);
}
    
