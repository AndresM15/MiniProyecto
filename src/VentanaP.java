    private void guardarContacto() {
         String correo = c4.getText();
        if (!correo.contains("@")) {
            JOptionPane.showMessageDialog(this, "El correo debe contener un '@'", "Error de validación", JOptionPane.ERROR_MESSAGE);
            return; // Detiene la ejecución del método si el correo es inválido
        }
    try {
        Contacto contacto = new Contacto();
        contacto.setNombre(c1.getText());
        contacto.setApellido(c2.getText());
        contacto.setTelefono(c3.getText());
        contacto.setCorreo(c4.getText());
        contacto.setDireccion(c5.getText());
        if (Soltero.isSelected()) {
            contacto.setEstadiCivil("Soltero");
        } else if (Casado.isSelected()) {
            contacto.setEstadiCivil("Casado"); 
        } else if (UnionLibre.isSelected()) {
            contacto.setEstadiCivil("Union libre"); 
        } else if (Divorciado.isSelected()) {
            contacto.setEstadiCivil("Divorciado"); 
        }
        listaContactos.add(contacto);
        refrescarTabla();
        JOptionPane.showMessageDialog(this, "Contacto añadido", "Sistema", JOptionPane.INFORMATION_MESSAGE);
        
        // Limpiar campos de texto
        c1.setText(null);
        c2.setText(null);
        c3.setText(null);
        c4.setText(null);
        c5.setText(null);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al agregar contacto");
    }
}
    
    public ArrayList getListaContactos () {
        return listaContactos;
    }
    
    public void refrescarTabla(){
        //Borrar todos los elemetos de la tabla
        
        while(mt.getRowCount()>0){
            mt.removeRow(0);
            
        }
        
        for(Contacto contacto: listaContactos){
            Object a[]=new Object[6];
            a[0]=contacto.getNombre();
            a[1]=contacto.getApellido();
            a[2]=contacto.getTelefono();
            a[3]=contacto.getCorreo();
            a[4]=contacto.getDireccion();
            a[5]=contacto.getEstadiCivil();
            mt.addRow(a);
            
            
        
        }
        
       
        
        jTable1.setModel(mt);
    }

